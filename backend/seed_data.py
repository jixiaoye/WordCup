def seed_all():
    """插入所有球队、球员、广告位和广告投放数据"""
    import logging
    logger = logging.getLogger(__name__)

    for t in TEAMS:
        db.session.add(Team(
            id=t["id"],
            name_cn=t["name_cn"],
            name_en=t["name_en"],
            flag_emoji=t["flag_emoji"],
            continent=t["continent"],
            tier=t["tier"],
            overall=t["overall"],
            color_primary=t["color_primary"],
            color_secondary=t["color_secondary"],
        ))

    db.session.flush()
    logger.info(f"已插入 {len(TEAMS)} 支球队")

    count = 0
    for team_id, team_players in PLAYERS.items():
        for p in team_players:
            name_cn, name_en, pos, number, pace, shooting, passing, defense, stamina, physical = p
            db.session.add(Player(
                team_id=team_id,
                name_cn=name_cn,
                name_en=name_en,
                position=pos,
                number=number,
                pace=pace,
                shooting=shooting,
                passing=passing,
                defense=defense,
                stamina=stamina,
                physical=physical,
            ))
            count += 1

    db.session.flush()
    logger.info(f"已插入 {count} 名球员")

    for z in AD_ZONES:
        db.session.add(AdZone(
            id=z["id"],
            position=z["position"],
            size_category=z["size_category"],
            width=z["width"],
            height=z["height"],
            label=z["label"],
        ))

    db.session.flush()
    logger.info(f"已插入 {len(AD_ZONES)} 个广告位")

    for c in AD_CAMPAIGNS:
        db.session.add(AdCampaign(
            advertiser_name=c["advertiser_name"],
            slogan=c["slogan"],
            bg_color=c["bg_color"],
            text_color=c["text_color"],
            zone_id=c["zone_id"],
            logo_text=c["logo_text"],
            is_active=True,
        ))

    db.session.flush()
    logger.info(f"已插入 {len(AD_CAMPAIGNS)} 条广告投放")

    for m in MASCOTS:
        db.session.add(Mascot(
            team_id=m["team_id"],
            name=m["name"],
            name_en=m["name_en"],
            icon=m["icon"],
            element=m["element"],
            rarity=m["rarity"],
            boost_attack=m["boost_attack"],
            boost_defense=m["boost_defense"],
            boost_speed=m["boost_speed"],
            boost_spirit=m["boost_spirit"],
            description=m["description"],
            description_en=m["description_en"],
        ))

    db.session.flush()
    logger.info(f"已插入 {len(MASCOTS)} 只萌宠吉祥物")
